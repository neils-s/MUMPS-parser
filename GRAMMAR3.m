    ; This routine augments array with the grammer for a code block with at least 'level' many leading dots.
    ; Note that this grammer will contain a recursive reference to the grammer for the code block with at
    ; least 'level'+1 many leading dots.
buildCodeBlockWithTemplate(level,array) 
    s level=+$g(level)
    i level>0,'$d(array("codeBlock_"_(level-1))) s %=$$buildCodeBlockWithTemplate(level-1,.array)
    ;
    n arrayNode
    s arrayNode="codeBlock_"_level
    ;
    ; This builds the part of an array representing a block of one or more lines that starts with (at least) 'level'-many dots
    s array(arrayNode)="delimList"
    s array(arrayNode,"content")="subtreeChain" 
    s array(arrayNode,"content",1)="token"
    s array(arrayNode,"content",1,"value")="eol" ; it's easiest to start the line with the end of the previous line
    s array(arrayNode,"content",2)="options" ; an optional tag label
    s array(arrayNode,"content",2,1)=""
    s array(arrayNode,"content",2,2)="token"
    s array(arrayNode,"content",2,2,"value")="label"
    s array(arrayNode,"content",3)="token" ; leading spaces
    s array(arrayNode,"content",3,"value")="ls"
    s array(arrayNode,"content",4)="token" ; The leading dots, if any
    s array(arrayNode,"content",4,"value")="lineInitializer_"_level
    s array(arrayNode,"content",5)="options" ; the line body and deeper lines
    s array(arrayNode,"content",5,1)="token"
    s array(arrayNode,"content",5,1,"value")="linebody"
    s array(arrayNode,"content",5,2)="subtreeChain"
    s array(arrayNode,"content",5,2,1)="token"
    s array(arrayNode,"content",5,2,1,"value")="linebodyWithBlockPush" ; argumentless DO statements are followed by deeper blocks
    s array(arrayNode,"content",5,2,2)="delimList"
    s array(arrayNode,"content",5,2,2,"content")=""
    s array(arrayNode,"content",5,2,2,"delimiter")="token"
    s array(arrayNode,"content",5,2,2,"delimiter","value")="codeBlock_"_(level+1)
    s array(arrayNode,"delimiter")=""
    s array(arrayNode,"generatorXecutable")="s %=$$buildCodeBlockWithTemplate^GRAMMER3("_(level+1)_",.mapPointer)" ; This code will get executed by the parser so the grammer always includes a "next" dot level
    ;
    i level=0 d
    . s array("lineInitializer_0")="literal"
    . s array("lineInitializer_0","value")=""
    i level>0 d
    . s array("lineInitializer_"_level)="subtreeChain"
    . s array("lineInitializer_"_level,"force")="noStore" ; we don't need the details of the deeper parsing
    . s array("lineInitializer_"_level,1)="token"
    . s array("lineInitializer_"_level,1,"value")="lineInitializer_"_(level-1)
    . s array("lineInitializer_"_level,2)="delimList" ; zero or more spaces
    . s array("lineInitializer_"_level,2,"content")=""
    . s array("lineInitializer_"_level,2,"delimiter")="token"
    . s array("lineInitializer_"_level,2,"delimiter","value")="SP"
    . s array("lineInitializer_"_level,3)="token" ; followed by a dot
    . s array("lineInitializer_"_level,3,"value")="period"
    ;
    q level
